public class SimpleMethodExample {
    // Main method where program execution begins
    public static void main(String[] args) {
        // Creating an instance of SimpleMethodExample
        SimpleMethodExample example = new SimpleMethodExample();
        
        // Calling the greet method on the instance
        example.greet();
    }

    // A simple method that prints a greeting message
    void greet() {
        System.out.println("Hello, welcome to learning Java methods!");
    }
}
