public class RelationalOperatorsExample {
    public static void main(String[] args) {
        // Declare variables
        int a = 10;
        int b = 5;
        int c = 10;
        System.out.println("\u266A"); // Output:  â™ª

        // Equal to
        boolean isEqual = (a == c);
        System.out.println("Is a equal to c? " + isEqual); // Output: true

        // Not equal to
        boolean isNotEqual = (a != b);
        System.out.println("Is a not equal to b? " + isNotEqual); // Output: true

        // Greater than
        boolean isGreater = (a > b);
        System.out.println("Is a greater than b? " + isGreater); // Output: true

        // Less than
        boolean isLess = (a < b);
        System.out.println("Is a less than b? " + isLess); // Output: false

        // Greater than or equal to
        boolean isGreaterOrEqual = (a >= c);
        System.out.println("Is a greater than or equal to c? " + isGreaterOrEqual); // Output: true

        // Less than or equal to
        boolean isLessOrEqual = (a <= c);
        System.out.println("Is a less than or equal to c? " + isLessOrEqual); // Output: true
    }
}








