public class WhileLoopExample { 
    public static void main(String[] args) { 
        int number = 1; // Initialize the counter
  
        while (number <= 5) { // Condition to keep looping 
            System.out.println("Count: " + number); 
            number++; // Increment the counter 
        } 
         
        System.out.println("Loop terminated. The final number is " + number); 
    } 
}
