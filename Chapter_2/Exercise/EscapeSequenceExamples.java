package Exercise;

public class EscapeSequenceExamples {
    public static void main(String[] args) {
        // Using double quotes
        System.out.println("This is a double quote: \"");

        // Using single quote
        System.out.println("This is a single quote: \'");

        // Using backslash
        System.out.println("This is a backslash: \\");

        // Using newline
        System.out.println("This is the first line.\nThis is the second line.");

        // Using carriage return
        System.out.println("This is before carriage return.\rThis is after.");

        // Using tab
        System.out.println("This is a tab:\tSee the space?");

        // Using backspace
        System.out.println("This is a backspace character\b.");

        // Using form feed
        System.out.println("This is a form feed:\fNew page?");

        // Combining multiple escape sequences
        System.out.println("Here is a tab:\tThen a new line:\nAnd a backslash:\\");

        // Escape sequences in a longer text
        System.out.println("She said, \"Java is versatile!\"\nAnd I replied, \'Indeed, it is!\'");
    }
}
